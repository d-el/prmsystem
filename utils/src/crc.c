/*!****************************************************************************
 * @brief CRC16 calculate
 * @param[in] dat - poinret to CRC data structure
 * @param[in] src – source pointer
 * @param[in] len - number of bytes to calculate
 * @return CRC16 result
 */
uint16_t crc16Calc(const crc16Struct_type *dat, const void *src, uint32_t len){
	const uint16_t	*table = dat->table;
	uint16_t	crc = dat->initVal;
	uint8_t		*pBuf = (uint8_t*)src;
	uint16_t	tmp;

	do{
		tmp = ((*pBuf++) ^ crc) & 0xff;
		crc >>= 8;
		crc ^= table[tmp];
		len--;
	}while (len);

	return crc ^ dat->finalXorValue;
}

 /*!****************************************************************************
 * @brief CRC16 calculate
 * @param[in] dat - poinret to CRC data structure
 * @param[in] src – source pointer
 * @param[in] len - number of bytes to calculate
 * @param[in] initVal - initial value
 * @return CRC16 result
 */
uint16_t crc16CalcWithInit(const crc16Struct_type *dat, const void *src, uint32_t len, uint16_t initVal){
	const uint16_t	*table = dat->table;
	uint16_t	crc = initVal;
	uint8_t		*pBuf = (uint8_t*)src;
	uint16_t	tmp;

	do{
		tmp = ((*pBuf++) ^ crc) & 0xff;
		crc >>= 8;
		crc ^= table[tmp];
		len--;
	}while (len);

	return crc ^ dat->finalXorValue;
}

/*!****************************************************************************
 * @brief CRC8 calculate
 * @param[in] dat - poinret to CRC data structure
 * @param[in] src – source pointer
 * @param[in] len - number of bytes to calculate
 * @return CRC8 result
 */
uint8_t crc8Calc(const crc8Struct_type *dat, const void *src, uint32_t len){
	const uint8_t	   *table = dat->table;
	uint8_t		crc = dat->initVal;
	uint8_t		*pBuf = (uint8_t*)src;

	do{
		crc = table[crc ^ *pBuf++];
		len--;
	}while (len);

	return crc ^ dat->finalXorValue;
}

